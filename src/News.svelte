<h3>Monthly newsletters</h3>
<p>
Regularly published newsletters provide timely reminders for students. These are geared toward students, parents or both. College-planning newsletters are typically two to eight pages long, outline the steps students should be taking each month and refer to more detailed information that is available in your handbook. We include items such as:
</p>
<ul>
    <li>Important upcoming events  </li>
    <li>Admission test registration dates</li>
    <li>Deadlines for key points in the application process </li>
    <li>College fairs </li>
    <li>Visits from college reps</li>
    <li>Financial aid seminars</li>
</ul>
